import random

def magic_8_ball():
    # Список возможных ответов от шара судьбы
    answers = [
        "Да, конечно!",
        "Вероятно, да",
        "Непредсказуемо, спроси позже",
        "Сложно сказать",
        "Не стоит рассчитывать на это",
        "Определённо, нет",
        "Мой ответ - нет",
        "Весьма сомнительно"
    ]

    # Запрос вопроса у пользователя
    question = input("Задайте ваш вопрос: ")

    # Получение случайного ответа из списка
    answer = random.choice(answers)

    # Вывод ответа
    print(f"Шар судьбы говорит: {answer}")

# Запуск программы
magic_8_ball()
